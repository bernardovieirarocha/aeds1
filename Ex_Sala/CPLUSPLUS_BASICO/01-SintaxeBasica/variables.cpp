#include <iostream>
using namespace std;

int main() {
    // Variáveis
    int a = 5;
    int b = 10;

    // Constantes
    const float PI = 3.14159;

    // Operações com variáveis
    int soma = a + b;
    int produto = a * b;

    // Exibindo resultados
    cout << "Soma: " << soma << endl;
    cout << "Produto: " << produto << endl;
    cout << "Valor de PI: " << PI << endl;

    return 0;
}
