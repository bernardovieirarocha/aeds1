#include <iostream>
using namespace std;

int main() {
    // Tipos de dados básicos
    int inteiro = 10;           // Inteiro
    float flutuante = 3.14;     // Número de ponto flutuante
    double grandeFlutuante = 3.141592653589793; // Número de ponto flutuante de dupla precisão
    char caractere = 'A';       // Caractere
    bool booleano = true;       // Booleano

    // Exibindo os valores
    cout << "Inteiro: " << inteiro << endl;
    cout << "Flutuante: " << flutuante << endl;
    cout << "Grande Flutuante: " << grandeFlutuante << endl;
    cout << "Caractere: " << caractere << endl;
    cout << "Booleano: " << booleano << endl;

    return 0;
}
